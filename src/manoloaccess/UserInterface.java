    private void tbl_eventsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_eventsMouseClicked
        // TODO add your handling code here:
      int r = tbl_events.getSelectedRow();

    if (r >= 0) {
        Object eventIdObj = tbl_events.getModel().getValueAt(r, 0); // COLUMN 0 = event_id

        if (eventIdObj != null) {
            String eventId = eventIdObj.toString();
            String sql = "SELECT e.event_id, v.name AS venue_name, e.name, e.event_date, e.description, e.image_data, e.category " +
                         "FROM events e JOIN venues v ON e.venue_id = v.venue_id WHERE e.event_id = ?";

            try {
                PreparedStatement ps = con.prepareStatement(sql);
                ps.setString(1, eventId);
                ResultSet rs = ps.executeQuery();

                if (rs.next()) {
                    // Fetch data
                    String name = rs.getString("name");
                    String eventDate = rs.getString("event_date");
                    String desc = rs.getString("description");
                    String category = rs.getString("category");
                    String venueName = rs.getString("venue_name");
                    Blob image = rs.getBlob("image_data");

                    // Convert Blob to byte array and save
                    byte[] bytea = image.getBytes(1, (int) image.length());
                    String path = "C:\\Users\\Admin\\Documents\\NetBeansProjects\\manoloAccess\\src\\mysqltonetbeans\\images\\img.jpg";
                    FileOutputStream fos = new FileOutputStream(path);
                    fos.write(bytea);
                    fos.close();

                    // Set values to UI fields
                    Title.setText(name);
                    Date.setText(eventDate);
                    Description.setText(desc);
                    Category.setText(category);
                    Venue.setText(venueName);

                    // Display image
                    ImageIcon icon = new ImageIcon(bytea);
                    Image scaled = icon.getImage().getScaledInstance(
                        EventImageContainer.getWidth(),
                        EventImageContainer.getHeight(),
                        Image.SCALE_SMOOTH
                    );
                    EventImageContainer.setIcon(new ImageIcon(scaled));
                } else {
                    JOptionPane.showMessageDialog(null, "No event found with that ID.");
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selected row has no event_id.");
        }
    } else {
        JOptionPane.showMessageDialog(null, "Please select a row first.");
    }
    }//GEN-LAST:event_tbl_eventsMouseClicked

    private void BuyTicket_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuyTicket_ButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbl_events.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(null, "Please select an event first.");
        return;
    }

    try {
        String eventIdStr = tbl_events.getValueAt(selectedRow, 0).toString(); // column 0 = event_id
        int selectedEventId = Integer.parseInt(eventIdStr);
        String sql = "SELECT e.*, v.name AS venue_name, t.ticket_type, t.seat_section, t.price, t.tickets_left " +
                     "FROM events e " +
                     "LEFT JOIN venues v ON e.venue_id = v.venue_id " +
                     "LEFT JOIN tickets t ON e.event_id = t.event_id " +
                     "WHERE e.event_id = ?";
        
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setInt(1, selectedEventId);
        ResultSet rs = ps.executeQuery();

        if (rs.next()) {
            int customerId = UserSession.getCustomerId();
            if (customerId <= 0) {
                JOptionPane.showMessageDialog(null, "No customer logged in. Please log in first.");
                return;
            }

            Reservation reservation = new Reservation();
            reservation.setEventData(
                selectedEventId,
                rs.getBytes("image_data"),
                rs.getString("name"),
                rs.getString("event_date"),
                rs.getString("description"),
                rs.getString("category"),
                rs.getString("venue_name"),
                rs.getString("ticket_type"),
                rs.getString("seat_section"),
                rs.getDouble("price"),
                rs.getInt("tickets_left")
            );
            reservation.setVisible(true);

            // âœ… Dispose current frame after opening the reservation window
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Event details not found.");
        }

        rs.close();
        ps.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_BuyTicket_ButtonActionPerformed

