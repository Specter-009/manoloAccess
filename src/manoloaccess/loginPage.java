    private void U_LoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_U_LoginButtonActionPerformed
        // TODO add your handling code here:
     String username = U_Username.getText().trim();
    String password = new String(U_Password.getPassword()).trim();

    // Validation
    if (username.isEmpty() || password.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Username and password are required.");
        return;
    }

    if (con == null) {
        JOptionPane.showMessageDialog(null, "Database connection is not established.");
        return;
    }

    System.out.println("Trying login with username: " + username + ", password: " + password);

    try {
        String sql = "SELECT customer_id, name FROM customers WHERE username = ? AND password = ?";
        try (PreparedStatement ps = con.prepareStatement(sql)) {
            ps.setString(1, username);
            ps.setString(2, password);  // Use hashed password if your DB stores it hashed

            try (ResultSet rs = ps.executeQuery()) {
                if (rs.next()) {
                    int customerId = rs.getInt("customer_id");
                    String customerName = rs.getString("name");

                    System.out.println("Login successful for user: " + customerName + " (ID: " + customerId + ")");
                    JOptionPane.showMessageDialog(null, "Login successful! Welcome, " + customerName);

                    // HIGHLIGHT: Store customerId in UserSession instead of setting it directly in UserInterface
                    UserSession.setCustomerId(customerId);
                    UserInterface userint = new UserInterface();
                    userint.setVisible(true);

                    dispose(); // Close login form
                } else {
                    JOptionPane.showMessageDialog(null, "Invalid username or password.");
                }
            }
        }
    } catch (SQLException e) {
        e.printStackTrace();  // Log full error
        JOptionPane.showMessageDialog(null, "Error during login: " + e.getMessage());
    }
    }//GEN-LAST:event_U_LoginButtonActionPerformed

