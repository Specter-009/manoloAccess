    private void login(String username, String password) {
        try (PreparedStatement ps = con.prepareStatement(
                "SELECT customer_id FROM customers WHERE username = ? AND password = ?")) {
            ps.setString(1, username);
            ps.setString(2, password); // Note: In production, hash passwords
            try (ResultSet rs = ps.executeQuery()) {
                if (rs.next()) {
                    int customerId = rs.getInt("customer_id");
                    UserSession.setCustomerId(customerId);
                    JOptionPane.showMessageDialog(null, "Login successful!");
                    new UserInterface().setVisible(true); // Navigate to UserInterface
                    dispose(); // Close LoginPage
                } else {
                    JOptionPane.showMessageDialog(null, "Invalid username or password.");
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Login error: " + e.getMessage());
        }
    }
    
   

